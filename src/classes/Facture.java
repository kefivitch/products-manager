package classes;

/**
 *
 * @author Lassad
 */
public class Facture {
    private String id;
    private String date;
    private String to;

    public Facture(String id, String date, String to) {
        this.id = id;
        this.date = date;
        this.to = to;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }
    
}
